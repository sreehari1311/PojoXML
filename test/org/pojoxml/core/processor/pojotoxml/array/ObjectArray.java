package org.pojoxml.core.processor.pojotoxml.array;

public class ObjectArray {
	private String name;
	private WrapperArray[] wrapperArray;
	private int age;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public WrapperArray[] getWrapperArray() {
		return wrapperArray;
	}
	public void setWrapperArray(WrapperArray[] wrapperArray) {
		this.wrapperArray = wrapperArray;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
}
